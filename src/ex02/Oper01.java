package ex02;

public class Oper01 {
    public static void main(String[] args) {
        int n1 = 2;
        int n2 = 4;
        System.out.println(n1+n2);
        System.out.println(n1-n2);
        System.out.println(n1*n2);
        System.out.println(n1/n2);

        // 결합 연산자
        System.out.println("n1+n2="+"6");
        System.out.println("n1+n2="+(n1+n2));
    }
}
